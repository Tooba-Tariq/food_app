class Person {
  String id;
  String firstName;
  String lastName;
  String username;
  String phoneNo;
  String status;
  String age;
  String image;
  String bio;

  Person({
    required this.age,
    required this.bio,
    required this.firstName,
    required this.id,
    required this.image,
    required this.lastName,
    required this.phoneNo,
    required this.username,
    required this.status,
  });
}
